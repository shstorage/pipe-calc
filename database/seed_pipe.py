def seed(reset: bool = False):
    db = SessionLocal()
    if reset:
        db.query(models.PipeSchedule).delete()
        db.commit()
        print("pipe_schedule 테이블 초기화 완료")

    inserted = skipped = 0
    for standard, dn, nps, schedule, identification, od_mm, wt_mm, mass_kg_m, od_in, wt_in, mass_lb_ft in PIPE_DATA:
        obj = models.PipeSchedule(
            standard=standard, dn=dn, nps=nps, schedule=schedule,
            identification=identification, od_mm=od_mm, wt_mm=wt_mm,
            mass_kg_m=mass_kg_m, od_in=od_in, wt_in=wt_in, mass_lb_ft=mass_lb_ft,
        )
        db.add(obj)
        try:
            db.commit()
            inserted += 1
        except Exception:
            db.rollback()
            skipped += 1
    db.close()
    print(f"pipe_schedule: inserted={inserted}, skipped={skipped}")


if __name__ == "__main__":
    parser = argparse.ArgumentParser()
    parser.add_argument("--reset", action="store_true", help="테이블 초기화 후 재삽입")
    args = parser.parse_args()
    seed(reset=args.reset)
